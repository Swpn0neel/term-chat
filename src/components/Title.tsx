// Map lowercase to uppercase since they are the same in this font
"abcdefghijklmnopqrstuvwxyz".split("").forEach(char => {
  FONT[char] = FONT[char.toUpperCase()];
});

const FALLBACK = ["████", "█  █", "████", "█  █", "█  █", "╚══╝"];

export function Title({ children, color }: TitleProps) {
  const theme = useTheme();
  const { stdout } = useStdout();
  const resolvedColor = color ?? theme.colors.primary;
  
  const chars = children.split('');
  const rows = 6;

  // Calculate approximate width of ASCII logo
  const totalWidth = chars.reduce((acc, ch) => {
    const charRows = FONT[ch] ?? FALLBACK;
    return acc + (charRows[0]?.length ?? 0);
  }, 0);

  // If terminal is too narrow OR too short, use simple text with a border
  const tooNarrow = stdout?.columns && stdout.columns < totalWidth + 10;
  const tooShort = stdout?.rows && stdout.rows < 25;

  if (tooNarrow || tooShort) {
    return (
      <Box borderStyle="double" borderColor={resolvedColor} paddingX={2} paddingY={0}>
        <Text color={resolvedColor} bold>
          {children.toUpperCase()}
        </Text>
      </Box>
    );
  }

  return (
    <Box flexDirection="column">
      {Array.from({ length: rows }, (_, rowIdx) => (
        <Box key={rowIdx} flexDirection="row">
          {chars.map((ch, charIdx) => {
            const charRows = FONT[ch] ?? FALLBACK;
            const rowStr = charRows[rowIdx] ?? "";
            return (
              <Text key={charIdx} color={resolvedColor}>
                {rowStr}
              </Text>
            );
          })}
        </Box>
      ))}
    </Box>
  );
}

const FALLBACK = ["████", "█  █", "████", "█  █", "█  █", "╚══╝"];

/**
 * TERMCHAT wordmark rendered as dynamic ASCII art —
 * using the same logic as the CLI Title component.
 */
export function AsciiLogo({ 
  className = "", 
  children = "TERMCHAT" 
}: { 
  className?: string,
  children?: string 
}) {
  const chars = children.split('');
  const rows = 6;

  const art = Array.from({ length: rows }, (_, rowIdx) => {
    return chars.map((ch) => {
      const charRows = FONT[ch] ?? FALLBACK;
      return charRows[rowIdx] ?? "";
    }).join('');
  }).join('\n');

  return (
    <pre
      aria-label={children}
      className={`max-w-full overflow-hidden whitespace-pre text-primary text-[5.5px] min-[400px]:text-[6.5px] sm:text-[10px] md:text-[13px] lg:text-[16px] ${className}`}
      style={{
        fontFamily: "'Courier New', Courier, monospace",
        fontWeight: "bold",
        lineHeight: 1,
        letterSpacing: 0,
        wordSpacing: 0,
        textShadow: "0 0 1px currentColor",
      }}
    >
      {art}
    </pre>
  );
}
